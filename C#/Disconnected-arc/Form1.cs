        private void button1_Click(object sender, EventArgs e)
        {
            DataRow dr = ds.Tables["Table_2"].NewRow();

            // Don't set Id here! It will be generated by the DB.
            dr["Name"] = textName.Text;
            dr["City"] = textCity.Text;

            ds.Tables["Table_2"].Rows.Add(dr);

            da.Update(ds, "Table_2");

            // Refresh DataSet to get new Id from database
            ds.Tables["Table_2"].Clear();
