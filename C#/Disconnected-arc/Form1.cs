        private void button2_Click(object sender, EventArgs e)
        {
            foreach (DataRow dr in ds.Tables["Table_2"].Rows)
            {
                if (dr["Id"].ToString() == textId.Text)
                {
                    dr["Name"] = textName.Text;
                    dr["City"] = textCity.Text;
                }
            }

            da.Update(ds, "Table_2");

            MessageBox.Show("Record Updated");
        }

        private void button3_Click(object sender, EventArgs e)
        {
            foreach (DataRow dr in ds.Tables["Table_2"].Rows)
            {
                if (dr["Id"].ToString() == textId.Text)
                {
                    dr.Delete();
                    break;
                }
            }

            da.Update(ds, "Table_2");

            MessageBox.Show("Record Deleted");
        }

        private void button4_Click(object sender, EventArgs e)
        {
            ds.Tables["Table_2"].Clear();

            da.Fill(ds, "Table_2");

            dataGridView1.DataSource = ds.Tables["Table_2"];
        }
